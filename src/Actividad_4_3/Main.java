	//Mostrar los elementos del directorio actual
	private static void ConsultarLista(FTPFile[] files,String direc2, JList list) throws IOException {
            Main m = new Main();
		if(files==null) return;
		
		DefaultListModel Listam=new DefaultListModel();
		
		
		m.lista.removeAll();
		
		try {
			ftp.changeWorkingDirectory(direc2);
		} catch (IOException e) {
			// TODO: handle exception
			e.printStackTrace();
		}
		
		direcSelec=direc2;
		
		Listam.addElement(direc2);
		
		for (int i = 0; i < files.length; i++) {
			if(!(files[i].getName()).equals(".") && !(files[i].getName()).equals("..")) {
				String f=files[i].getName();
				
				if(files[i].isDirectory()) f="(DIR) "+f;
				
				Listam.addElement(f);
			} 
		}
		
		try {
			list.setModel(Listam);
		} catch (NullPointerException n) {
			;
			// TODO: handle exception
		}
	}
