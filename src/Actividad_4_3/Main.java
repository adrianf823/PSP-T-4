private void Descargar(String nomCompleto, String nomFichero) {
    System.out.println(nomCompleto);
		String acd="";
		String carpetaDestino="";
		JFileChooser f=new JFileChooser();
		
		f.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
		f.setDialogTitle("Selecciona el Directorio");
		
		int returnVal=f.showDialog(null, "Descargar");
		if(returnVal==JFileChooser.APPROVE_OPTION) {
			File file=f.getSelectedFile();
			carpetaDestino=(file.getAbsolutePath()).toString();
			acd=carpetaDestino+File.separator+nomFichero;
			try {
				ftp.setFileType(FTP.BINARY_FILE_TYPE);
				BufferedOutputStream out=new BufferedOutputStream(new FileOutputStream(acd));
				//El momento de descargarlo
				if(ftp.retrieveFile(nomCompleto, out))
					JOptionPane.showMessageDialog(null, nomFichero+" se ha descargado con exito");
				else
					JOptionPane.showMessageDialog(null, nomFichero+" no se ha podido descargar");
				out.close();
			} catch (IOException e) {
				e.printStackTrace();
				// TODO: handle exception
			}
		}
	}
